return {
  {
    "gbprod/cutlass.nvim",
    lazy = false,
    config = function()
      require("cutlass").setup({
        -- 使用 m 作為剪下（cut）鍵
        cut_key = "m",
        -- 覆蓋預設的 d 行為
        override_del = true,
        -- 排除的檔案類型
        exclude = {},
        -- 暫存器設定
        registers = {
          select = "_", -- 選取時使用黑洞暫存器
          delete = "_", -- 刪除時使用黑洞暫存器
          change = "_", -- 變更時使用黑洞暫存器
        },
        -- ========================================================================
        -- 行為說明:
        -- d/x/c 等操作不會覆蓋剪貼簿（使用黑洞暫存器 _）
        -- m 才是真正的「剪下」（cut），會存入剪貼簿
        -- 例: mm 剪下整行, miw 剪下單字
        -- ========================================================================
      })
    end,
  },
}
